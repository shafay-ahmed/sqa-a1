def is_greater(a, b):
    if a > b:
        return True
    else:
        return False


def is_smaller(a, b):
    if a < b:
        return True
    else:
        return False


def is_equal(a, b):
    if a == b:
        return True
    else:
        return False


def get_type(a):
    return type(a)


def less_than_zero(a):
    if a < 0:
        return True
    else:
        return False
